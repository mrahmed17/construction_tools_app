export interface User {
  id: string;
  name: string;
  phone: string;
  password: string;
  businessName: string;
  createdAt: string;
}

export interface Company {
  id: string;
  name: string;
  products: string[];
  thicknesses: number[];
  sizes: number[];
}

export interface Product {
  id: string;
  category: string;
  company?: string;
  type: string;
  color?: string;
  thickness?: number;
  size?: number;
  purchasePrice: number;
  salePrice: number;
  stock: number;
  minStock: number;
  unit: 'piece' | 'sheet' | 'foot';
  image?: string;
  supplierId?: string;
}

export interface CartItem {
  productId: string;
  product: Product;
  quantity: number;
  selectedThickness?: number;
  selectedSize?: number;
  selectedColor?: string;
}

export interface Supplier {
  id: string;
  name: string;
  phone: string;
  address: string;
  company: string;
}

export interface Sale {
  id: string;
  customerName: string;
  customerAddress: string;
  items: CartItem[];
  subtotal: number;
  discount: number;
  total: number;
  profit: number;
  date: string;
}

export interface StockMovement {
  id: string;
  productId: string;
  type: 'in' | 'out';
  quantity: number;
  reason: string;
  date: string;
}

